    private void SanatciAramaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SanatciAramaKeyReleased
        // TODO add your handling code here:
        String ara = SanatciArama.getText();
        
        Sanatci_Dinamik_Ara(ara);
    }//GEN-LAST:event_SanatciAramaKeyReleased

    public void Sanatcilari_Goruntule() {
        
        modelSanatci.setRowCount(0);
        
        ArrayList<Sanatci> sanatcilar = new ArrayList<Sanatci>();
        
        sanatcilar = adminSanatciIslemleri.SanatcilariGetir();
        
        if (sanatcilar != null ) {
            
            for (Sanatci sanatci : sanatcilar) {
                Object[] eklenecek = {sanatci.getSanatciID(), sanatci.getSanatciAd(), sanatci.getSanatciUlke()};
                
                modelSanatci.addRow(eklenecek);
            }
        }
    }
    
    public void Sanatci_Dinamik_Ara(String ara){
        
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(modelSanatci);
        
        SanatciTablosu.setRowSorter(tr);
        
        tr.setRowFilter(RowFilter.regexFilter(ara));
        
    }
