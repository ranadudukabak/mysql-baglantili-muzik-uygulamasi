    private void GuncelleButonuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuncelleButonuActionPerformed
        // TODO add your handling code here:
        String sarkiAd  =  SanatciAdiAlani.getText();
        String sarkiTarih = SanatciUlkesiAlani.getText();
        
        int selectedrow = SanatciTablosu.getSelectedRow();
        
        if(selectedrow != -1) {
            int id = (int) modelSanatci.getValueAt(selectedrow, 0);
            adminSanatciIslemleri.SanatciGuncelle(id, sarkiAd, sarkiTarih);
            Sanatcilari_Goruntule();
        }
    }//GEN-LAST:event_GuncelleButonuActionPerformed

